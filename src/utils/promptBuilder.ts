import type { ChatMessage } from '@/types/chat';
import type { PromptRule } from '@/types/promptRule';

/** 评分执行规范，放在规则和JD之后、摘要指令之前 */
const SCORING_INSTRUCTION = `

【评分执行规范】
评分是确定性计算过程，而非主观判断。请严格按以下四步逐项执行，禁止凭印象或感觉给分：

步骤1·事实提取：
对每个评分项，从简历中逐字提取与该项直接相关的客观事实（学历名称、工作年限数字、技能名称、项目成果等），
以原文引用形式列举。无法从简历中直接找到原文的，不得推断或臆造事实。

步骤2·条件匹配：
将步骤1提取的原文事实，逐一对照该评分项的命中条件和量化档位条款，
明确标注每一事实的"命中/未命中"状态及所匹配的具体档位条款编号。
事实不完全满足某档位全部条件的，不得视为命中该档位。

步骤3·得分计算：
仅依据步骤2的命中结果，机械套用档位分值：
- 命中某档位 → 得分 = 该档位的明确分值
- 未命中任何档位 → 得分 = 0
- 事实不完全命中任何档位 → 取最近的不超过档位分值，禁止自创中间分值
禁止使用"大约"、"综合来看"、"基本符合"等模糊表述确定得分。

步骤4·验算确认：
所有评分项完成后，逐项列出得分并在输出末尾展示加法验算：
项1(XX分) + 项2(XX分) + ... + 项N(XX分) = 总分(XX分)
总分必须严格等于各项之和，不得为主观估算值或近似值。

【输出模板（强制性）】
每个评分项必须严格按以下模板输出，不得省略任何子标题、不得改变顺序、不得合并步骤：

### 评分项N：[评分项名称]（满分[XX]分）

**步骤1·事实提取**
- [原文事实1]："简历原文片段"
- [原文事实2]："简历原文片段"
（无相关事实则仅写一行：无相关事实）

**步骤2·条件匹配**
- [事实1] → 命中 · 档位条款[X.X]
- [事实2] → 未命中 · 不满足[X.X]中的[具体条件]
（无可匹配事实则仅写一行：无可匹配事实）

**步骤3·得分计算**
命中档位：[档位名称/未命中任何档位]
该项得分：[数字]分

---

（以上模板重复每个评分项，最后一项之后不输出分隔线）

所有评分项完成后，输出验算：

**步骤4·验算确认**
评分项1([XX]分) + 评分项2([XX]分) + ... + 评分项N([XX]分) = 总分([XX]分)

规则：
1. 每个子标题（步骤1/2/3）都必须出现，禁止省略或合并
2. 步骤1/2用无序列表逐条展示，事实→条款→分值的链条必须可见
3. 步骤3只输出两个确定性字段：档位名+数字分值，禁止模糊表述
4. ---分隔线让各评分项视觉隔离，最后一项后不加分隔线`;

/** 追加到 system prompt 末尾的摘要指令 */
const SUMMARY_INSTRUCTION = `

【输出格式要求】
请在报告正文最末尾（有且只能出现一次）用如下 HTML 注释包裹核心摘要字段（渲染时不会显示该注释），采用一行一个属性的形式：
<!--SUMMARY_START-->
姓名: 值
年龄: 值
专业: 值
学历: 值
总分: 值
最终建议: 值
<!--SUMMARY_END-->
字段名和值必须严格为以上6项，不要添加其他字段。每行格式为"字段名: 值"，值中不要包含换行。
其中"总分"必须与步骤4验算结果完全一致，严格等于各项得分之算术累加和，不得为主观估算值或近似值。
此摘要块只能在输出最末尾出现一次，不要在中间或开头重复出现。`;

/** 将用户输入与规则组合成对话消息：规则作为 system，用户输入作为 user */
export function buildMessages(text: string, rule: PromptRule): ChatMessage[] {
  let systemContent = rule.rule;
  if (rule.jobDescription) {
    systemContent += `\n\n【岗位JD原始文本】\n以下是本评分框架对应的岗位JD原始文本，作为评估的溯源依据：\n${rule.jobDescription}`;
  }
  systemContent += SCORING_INSTRUCTION + SUMMARY_INSTRUCTION;
  return [
    { role: 'system', content: systemContent },
    { role: 'user', content: text },
  ];
}

/** 生成可读的提示词预览（用于 UI 展示） */
export function buildPromptPreview(text: string, rule: PromptRule): string {
  let preview = `【系统指令】\n${rule.rule}`;
  if (rule.jobDescription) {
    preview += `\n\n【岗位JD原始文本】\n${rule.jobDescription}`;
  }
  preview += `\n\n【用户输入】\n${text}`;
  return preview;
}
